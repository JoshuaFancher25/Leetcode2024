# implicit line continuation with parentheses in python

print("hello" 
      "world")

print(3
      + 4)

print(3, \
      4)

# Line continuation character is the backslash "\"
# Line continuation character is the \ backslash character
# Backslash character is also known as the escape character
# character patterns with the backslash are used in strings
# e.g. \n - for a new line
# \t for a tab

print("Hello \nWorld")  # the backslash puts a new line in the string 